void tokenize(std::string const& str, const char delim,
	std::vector<std::string>& out)
{
	size_t start;
	size_t end = 0;

	while ((start = str.find_first_not_of(delim, end)) != std::string::npos)
	{
		end = str.find(delim, start);
		out.push_back(str.substr(start, end - start));
	}
}

bool fun(CString out, CString inp) {
	if (out.Right(4) != ".txt") {
		return false;
	}

	if (inp.Right(4) != ".txt") {
		return false;
	}

	std::string line;
	string s = "";
	std::list < string> stInput{  };

	std::ifstream in(inp);
	if (in.is_open())
	{
		while (std::getline(in, line))
		{
			stInput.push_back(line);
		}
	}

	std::list < string> stSyn{  };

	std::ifstream syn("syn.txt");
	if (syn.is_open())
	{
		while (std::getline(syn, line))
		{
			stSyn.push_back(line);
		}
	}

	int k = 0;
	std::list < string> result{  };

	for (string i : stInput) {

		auto l_front = stSyn.begin();
		std::advance(l_front, k);
		string v = *l_front;
		const char delim = ';';

		std::vector<std::string> o;
		tokenize(v, delim, o);
		int t = o.size() ;
		int ran = rand() % t + 0;
		result.push_back(o[ran]);

		k++;
	}


	ofstream fout(out);
	for (string i : result) {
		fout << i << std::endl;
	}
	fout.close();
	return true;


}

void CMFCApplication1Dlg::OnBnClickedOk()
{
	CString out;
	_output.GetWindowTextW(out);

	CString inp;
	_input.GetWindowTextW(inp);
	
	if (fun(out, inp)) {
		AfxMessageBox(L"Correct");
		CDialogEx::OnOK();
	}
	else {
		AfxMessageBox(L"Error!!!!!!!!!");
		CDialogEx::OnOK();
	}
}


